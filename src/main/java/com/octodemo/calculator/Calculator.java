package com.octodemo.calculator;

public class Calculator {
	public double add(double a, double b) {
		return a+b;
	}

	public double sub(double a, double b) {
		return a-b;
	}

	public double multiply(double a, double b) {
		return a*b;

	}

	public double div(double a, double b) {
		return a/b;
	}


}
